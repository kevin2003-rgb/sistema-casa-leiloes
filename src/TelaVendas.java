 public void listarVendidos() {
    try {
        ProdutosDAO dao = new ProdutosDAO();

        ResultSet rs = dao.listarProdutosVendidos();

        DefaultTableModel model =
        (DefaultTableModel) tabelaVendas.getModel();

        model.setNumRows(0);

        while(rs.next()) {

            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("nome"),
                rs.getString("status")
            });
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}
    
    
    
    
    
    
    
    
